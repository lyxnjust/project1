#include<cstdio>
#include<iostream>
using namespace std;
int main()
{
    int a,b,c;
    cin>>a>>b>>c;
    int x,y,z;
    x=max(a,max(b,c));
    y=min(a,min(b,c));
    z=a+b+c-x-y;
    cout<<y<<endl;
    cout<<z<<endl;
    cout<<x<<endl;
    cout<<endl;
    cout<<a<<endl;
    cout<<b<<endl;
    cout<<c<<endl;
    return 0;
}

